#include <stdio.h>

int main()
{
    // Relational Operators
    // (>, <, >=, <=, ==, !=)

    int a = 10, b = 20;

    printf("a > b  : %d\n", a > b);   // 0 (false)
    printf("a < b  : %d\n", a < b);   // 1 (true)
    printf("a >= b : %d\n", a >= b);  // 0 (false)
    printf("a <= b : %d\n", a <= b);  // 1 (true)
    printf("a == b : %d\n", a == b);  // 0 (false)
    printf("a != b : %d\n", a != b);  // 1 (true)

    return 0;
}
